/**
 * op_add - prints a name as is
 * @a: is an int
 * @b: is an int
 * Return: int.
 */
int op_add(int a, int b)
{
    return (a + b);
}

/**
 * op_sub - print a name in uppercase
 * @a: is an int
 * @b: is an int
 * Return: Nothing.
 */
int op_sub(int a, int b)
{
return (a - b);
}

/**
 * op_mul - print a name in uppercase
 * @a: is an int
 * @b: is an int
 * Return: Nothing.
 */
int op_mul(int a, int b)
{
return (a * b);
}

/**
 * op_div - print a name in uppercase
 * @a: is an int
 * @b: is an int
 * Return: Nothing.
 */
int op_div(int a, int b)
{
return (a / b);
}

/**
 * op_mod - print a name in uppercase
 * @a: is an int
 * @b: is an int
 * Return: Nothing.
 */
int op_mod(int a, int b)
{
return (a % b);
}
